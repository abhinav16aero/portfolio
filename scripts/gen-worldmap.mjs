import { createRequire } from "node:module";
import { writeFileSync } from "node:fs";

const require = createRequire(process.cwd() + "/x.js");
const DottedMap = require("dotted-map").default;

// Dotted world map. White dots on transparent so it can be used as a CSS mask
// (the element's background-color = theme color shows through → theme-aware,
// single element, no thousands of DOM nodes).
const map = new DottedMap({ height: 44, grid: "diagonal" });
let svg = map.getSVG({ radius: 0.32, color: "#ffffff", shape: "circle", backgroundColor: "transparent" });
// shrink: round coordinates to 1 decimal
svg = svg.replace(/(cx|cy|r)="([\d.]+)"/g, (_m, k, v) => `${k}="${Math.round(parseFloat(v) * 10) / 10}"`);

const viewBox = svg.match(/viewBox="([^"]+)"/)[1];

writeFileSync("public/world-dots.svg", svg);

const cities = [
  { label: "Toronto", lat: 43.6532, lng: -79.3832, big: true },
  { label: "Vancouver", lat: 49.2827, lng: -123.1207 },
  { label: "Kingston", lat: 44.2312, lng: -76.486 },
  { label: "Cairo", lat: 30.0444, lng: 31.2357 },
  { label: "Mansoura", lat: 31.0409, lng: 31.3785 },
  { label: "Shenzhen", lat: 22.5431, lng: 114.0579 },
  { label: "Tunis", lat: 36.8065, lng: 10.1815 },
  { label: "London", lat: 51.5074, lng: -0.1278 },
];

const pins = cities.map((c) => {
  const p = map.getPin({ lat: c.lat, lng: c.lng });
  return {
    label: c.label,
    x: Math.round(p.x * 10) / 10,
    y: Math.round(p.y * 10) / 10,
    ...(c.big ? { big: true } : {}),
  };
});

const out = `// AUTO-GENERATED by scripts/gen-worldmap.mjs — pins for the Contact world map.
export const WORLD_VIEWBOX = ${JSON.stringify(viewBox)};
export type WorldPin = { label: string; x: number; y: number; big?: boolean };
export const WORLD_PINS: WorldPin[] = ${JSON.stringify(pins, null, 2)};
`;
writeFileSync("data/world-map.ts", out);
console.log("world-dots.svg bytes:", svg.length, "| viewBox:", viewBox, "| pins:", pins.length);
